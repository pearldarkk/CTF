bytes = [
    0xd, 0x13, 0x17, 0x11, 0x2, 0x1, 0x20, 0x1d, 0xc, 0x2, 0x19, 0x2f, 0x17,
    0x2b, 0x24, 0x1f, 0x1e, 0x16, 0x9, 0xf, 0x15, 0x27, 0x13, 0x26, 0xa, 0x2f,
    0x1e, 0x1a, 0x2d, 0xc, 0x22
]
flag = 'EIS{'
xor = 'GONDPHyGjPEKruv{{pj]X@rF'
semiflag = ''

for i in range(0, len(xor)):
    semiflag += chr(((ord(xor[i]) ^ bytes[i]) - 0x48) ^ 0x55)

for i in range(0, len(semiflag)):
    if ord('Z') >= ord(semiflag[i]) >= ord('A'):
        flag += chr(ord(semiflag[i]) + 0x20)
    elif semiflag[i] == '_':
        flag += semiflag[i]
    else:
        flag += chr(ord(semiflag[i]) - 0x20)
flag += '}'

print(flag)
